public class Idioma {
    private String nom;
    private String referencia;

    public void setNom(String Nom) {
        this.nom = Nom;
    }

    public void setReferencia(String Referencia) {
        this.referencia = Referencia;
    }

    public String getNom() {
        return nom;
    }

    public String getReferencia() {
        return referencia;
    }
     
    
}
